						<ol class="text-md pl-2">
                            <li>Energy Label – This is the new label title.</li>
                            <li>QR Code – This links to the EPREL database. Consumers can scan this and get all the EU label information in a tabular form.</li>
                            <li>Supplier name - Trade Name or Trade Mark of the supplier</li>
                            <li>Tyre type identifier – This is a unique article number that identifies the product</li>
                            <li>Tyre Size – this shows the tyre size designation, load-capacity index and speed category symbol</li>
                            <li>Tyre Class – This identifies the class of the tyre i.e. C1(Car &amp; SUV), C2(Van &amp; Light Truck) or C3(Truck and Bus).</li>
                            <li>Fuel Efficiency pictogram – depending on the tyre’s rolling resistance its fuel efficiency is graded from A all the way down to E. With A denoting the best fuel efficiency. </li>
                            <li>Wet Grip pictogram – This pictogram shows how well the tyre will perform in wet conditions. With performance graded from A down to E, with A signifying the shortest braking distance on wet roads.</li>
                            <li>External rolling noise pictogram – This measures the noise levels in dB(decibels), followed by a noise class which replaces the existing sound bars. The noise class is graded from A down to C.</li>
                            <li>Snow grip pictogram (3PMSF) – This pictogram is mainly shown on winter and all weather tyres. It shows that the tyre is suited for performance in severe snow conditions. This is also known as the 3PMSF (3 Peak Mountain Snow Flake) or the Alpine Symbol.</li>
                            <li>Ice grip pictogram – This is shown on winter tires that have outstanding ice performance which has been confirmed by a defined ice grip test. These tyres are designed for extremely severe climate conditions such as those in the Nordics where roads are covered with ice or hard compact snow. Such tyres should not be used in less severe climate conditions.</li>
